def sumar(n1,n2):
    return n1 + n2

def restar(n1,n2):
    return n1 - n2

def multiplicar(n1,n2):
    return n1 * n2

def dividir(n1,n2):
    return n1 / n2
n1 = int (input("Ingresa el numero 1: "))
n2 = int (input("Ingresa el numero 2: "))

print("Suma: ", sumar(n1, n2))
print("Resta: ", restar(n1, n2))
print("Multicar: ", multiplicar(n1, n2))
print("Dividir: ", dividir(n1, n2))
